    private void MakeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MakeActionPerformed
        
        String selectedMake = Make.getSelectedItem().toString();
        
        Model.setEnabled(true);
        
        try
        {
            Connection conM=DBconnection.connect();
            
            Statement st=conM.createStatement();
            
            String getModel="SELECT Model FROM car WHERE Make='"+selectedMake+"'";
            
            ResultSet rs=st.executeQuery(getModel);
            
            while (rs.next())
            {
                 Model.addItem(rs.getString("Model"));
            }
        }
            catch(Exception e)
            {
              System.out.println(e);  
            }
        
    }//GEN-LAST:event_MakeActionPerformed
    
