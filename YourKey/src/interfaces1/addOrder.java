    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
         Add_Order order1=new Add_Order();
          String Customer_name=cus_name.getText();
        
        String selectedMake = Make.getSelectedItem().toString();
        
        String selectedModel = Model.getSelectedItem().toString();
        
        if (O_by_day.isSelected())
        {
            String delivered_day=d_delivered_day.getSelectedItem().toString();
            String delivered_month=d_delivered_month.getSelectedItem().toString();
            String delivered_year=d_delivered_year.getSelectedItem().toString();
            
            Date Delivered_day=ValidateDateorTime.ConvertStringtoDate(delivered_year,delivered_month,delivered_day);
            
            
            String return_day=d_return_day.getSelectedItem().toString();
            String return_month=d_return_month.getSelectedItem().toString();
            String return_year=d_return_year.getSelectedItem().toString();
            
            Date Returned_day=ValidateDateorTime.ConvertStringtoDate(return_year,return_month,return_day);
           
            
            ValidateDateorTime validate=new ValidateDateorTime();
            
            validate.setDates(Delivered_day,Returned_day);
            
            boolean validate_result=validate.validateDates();
            
            if ( validate_result==false)
            {
                JOptionPane.showMessageDialog(null,"Invalid date");
            }
            
            else
            {
                order1.RetrievePrice(selectedModel,selectedMake);
                order1.CalculateAmount(delivered_year, delivered_month, delivered_day, return_year, return_month, return_day);
                order1.insertFinalValues();
                order1.AddRentRecord(Customer_name);
            }
        }
         
         if (O_by_time.isSelected())
        {
            String delivered_day=t_delivered_day.getSelectedItem().toString();
            String delivered_month=t_delivered_month.getSelectedItem().toString();
            String delivered_year=t_delivered_year.getSelectedItem().toString();
            
            String delivered_date=delivered_year+"/"+delivered_month+"/"+delivered_day;
            
            String delivered_hour=t_delivered_hour.getSelectedItem().toString();
            String delivered_minute=t_delivered_minute.getSelectedItem().toString();
            
            Date Delivered_time=ValidateDateorTime.convertStringtoTime(delivered_year,delivered_month,delivered_day,delivered_hour,delivered_minute);
            
            String returned_hour=t_return_hour.getSelectedItem().toString();
            String returned_minute=t_return_minute.getSelectedItem().toString();
            
            Date Return_time=ValidateDateorTime.convertStringtoTime(delivered_year,delivered_month,delivered_day,returned_hour,returned_minute);
            
            ValidateDateorTime validate=new ValidateDateorTime();
            
            validate.setTimes(Delivered_time,Return_time);
            
            boolean validate_result=validate.validateTime();
            
            if (validate_result==false)
            {
                JOptionPane.showMessageDialog(null,"Invalid date");
            }
            
            else 
            {
                order1.RetrievePrice(selectedModel,selectedMake);
                order1.CalculateAmount(delivered_date, delivered_hour, delivered_minute, returned_hour, returned_minute);
                order1.insertFinalValues();
                order1.AddRentRecord(Customer_name);
            }
            
            
        }
    }//GEN-LAST:event_jButton3ActionPerformed

