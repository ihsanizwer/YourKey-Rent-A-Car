    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        selectedCars = new Vector();
        current=0;
        String seats = noOfSeatTxt.getText();
        ValidateMe v = new ValidateMe();
        int nSeats = 0;
        if(v.isValidNoSeats(seats)){
            nSeats = Integer.parseInt(seats);
        }

        DBConnection db = new DBConnection("SELECT * FROM car WHERE Seats LIKE '"+nSeats+"' AND CarType LIKE '"+carType+"'",1);
        ResultSet rs = db.getRS();
        try {
            int i = 1;
            while(rs.next()){
                res = res+Integer.toString(i)+". ";
                carIDs.add(i-1,Integer.toString(rs.getInt("CarID")));
                res = res+rs.getString("Model")+" ";
                res = res+rs.getString("Make")+" ";
                res = res+rs.getString("Year")+" ";
                if(rs.getBoolean("Taken"))
                res = res + "   Availability : Unavailable";
                else
                res = res + "   Availability : Available";
                selectedCars.add(i-1, res);
                res="";
                i++;
            }

            db.closeCon();
        } catch (SQLException ex) {
            Logger.getLogger(VCars.class.getName()).log(Level.SEVERE, null, ex);
        }
        setResults();
    }//GEN-LAST:event_searchBtnActionPerformed

