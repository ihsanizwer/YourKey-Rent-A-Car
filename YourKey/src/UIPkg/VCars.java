  private void setResults(){
        
        if(!(selectedCars.isEmpty()) & current==0 & selectedCars.size()==1){
            prevBtn.setVisible(true);
            nextBtn.setVisible(true);
            imgLbl.setVisible(true);
            prevBtn.setEnabled(false);
            nextBtn.setEnabled(false);
            jLabel4.setText(selectedCars.get(current).toString());
            imgLbl.setIcon(new ImageIcon(getClass().getResource("/media/"+carIDs.get(current)+".png")));
        }else if(!(selectedCars.isEmpty()) & current==0 & selectedCars.size()>1){
            prevBtn.setVisible(true);
            nextBtn.setVisible(true);
            imgLbl.setVisible(true);
            prevBtn.setEnabled(false);
            nextBtn.setEnabled(true);
            jLabel4.setText(selectedCars.get(current).toString());
            imgLbl.setIcon(new ImageIcon(getClass().getResource("/media/"+carIDs.get(current)+".png")));
        }else if(!(selectedCars.isEmpty()) & current ==(selectedCars.size()-1)){
            prevBtn.setVisible(true);
            nextBtn.setVisible(true);
            imgLbl.setVisible(true);
            nextBtn.setEnabled(false);
            prevBtn.setEnabled(true);
            jLabel4.setText(selectedCars.get(current).toString());
            imgLbl.setIcon(new ImageIcon(getClass().getResource("/media/"+carIDs.get(current)+".png")));
        }else if(!(selectedCars.isEmpty())){
            prevBtn.setVisible(true);
            nextBtn.setVisible(true);
            imgLbl.setVisible(true);
            prevBtn.setEnabled(true);
            nextBtn.setEnabled(true);
            jLabel4.setText(selectedCars.get(current).toString());
            imgLbl.setIcon(new ImageIcon(getClass().getResource("/media/"+carIDs.get(current)+".png")));
        }else if(selectedCars.isEmpty()){
            JOptionPane.showMessageDialog(null, "No cars found. Please choose a category, type the number of seats and try again!");
        }
        
        
        
       
    }
